
suma = 0    # variabla que acumula la suma

for i in range(1,11):   # Rang del 1 al 10 Realment ya que comença per 0
    suma += i   # cada vegada que passa per aquí li sumen i    
    print(f"La suma dels números en cada iteración es: {suma}")  # Imprimeix la suma de cada iterció   
print(f"La suma final dels 10 primers números es: {suma}")   # Imprimeix la suma total després de termina el bucle for
    