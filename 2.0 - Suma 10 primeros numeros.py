
suma = 0    # variables que acumula la suma

for i in range(1,11):   # Rango del 1 al 10 Realment ya que comença pero 0
    suma += i   # cada vegada que pasa per qui li sumen i    
    print(f"La suma de los numeros en cada iteración: {suma}")  # imprimeix la suma de cada iterció   
print(f"La suma final de los 10 primeros numeros es: {suma}")   # imprimeix la suma total després de termina el bucle for
    